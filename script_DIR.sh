#!/bin/bash

# Проверка, что каталог передан в аргументе
if [ -z "$1" ]; then
  echo "Использование: $0 <путь_к_каталогу>"
  exit 1
fi

DIR="$1"

# Проверяем, что это каталог
if [ ! -d "$DIR" ]; then
  echo "Ошибка: $DIR не является каталогом"
  exit 1
fi

# Перебор файлов в каталоге
for FILE in "$DIR"/*; do
  if [ -f "$FILE" ]; then
    echo "Обрабатываем файл: $FILE"
    # Пример действия: вывести размер файла
    SIZE=$(stat -c%s "$FILE")
    echo "Размер файла: $SIZE байт"
    echo "---------------------------"
  fi
done
